<h2>Рейтинг надёжности заёмщика - очень важный и нужный параметр!</h2>

	<p style="margin:5px 20px 0">Для чего вообще нами в середине 2016 года был введён рейтинг надёжности заёмщика?</p>
	<p style="margin:5px 20px 0"><strong>Дело в том, что поскольку выдача кредита одним инвестором делается нескольким заёмщикам одновременно, 
	самой главной целью была задача - удовлетворить пожелания и потребности всех заёмщиков инвестора в:</strong><br>
	1) Скорости выдачи кредита;<br>2) Условиях кредитования;<br>3) Удобном городе и месте выдачи;<br>4) 
	Удобном дне и времени выдачи.
	
	<br><br><strong>Но проблема в том, что 100% честно по отношению ко всем 
	заёмщикам, сделать это не получается никогда, так как:</strong><br>1) Инвестор берёт несколько кредитов и все они, 
	пусть и весьма не значительно, но естественно берутся под разные ставки и 
	условия, так как от банка к банку условия разнятся;<br>2) С датой выдачи естественно всем 
	заёмщикам не угодишь и подстраиваясь под кого-то одного, мы доставляем 
	дискомфорт кому-то другому;<br>3) Выбирая город выдачи мы как правило 
	выбираем один из крупнейших городов области (региона), но естественно в 
	данном случае иногда некоторым заёмщикам до города выдачи приходится 
	преодолевать иногда до сотни киллометров.</p>
		
<h2 style="margin:25px 0 0 0">Кому вершки, а кому корешки?</h2>

<p>Для того, чтобы всё было честно, нужно, чтобы каждый заёмщик получал по 
заслугам. В связи с этим и была введена рейтинговая система. Рейтинг надёжности, если он НЕ нулевой, НЕ влияет на решение по заявке 
на кредит, то есть даже если у Вас низкий рейтинг, мы всё равно Вас 
прокредитуем, так как наше кредо - доверительное кредитование, НО чем ниже 
рейтинг, тем меньше клиент "получит". Меньше внимания, хуже условия 
кредитования, невыгодные даты и города выдачи... </p>
	<p>Рейтинг зависит от нескольких факторов, некоторые 
	из которых не раскрываются нами, но в случае если они повлияли конкретно на 
	Ваш рейтинг, то 
	в личном диалоге мы поясняем что именно сыграло негативную роль в Вашем 
	рейтинге надёжности. Мы постоянно анализируем различные данные, проводим иследования и аналитичские срезы, цель которых добиться получения информации о качестве клиента по косвенным не классическим признакам, 
	то есть мы не требуем справок о доходах и залог. Мы оцениваем совсем другие 
	показатели. 
И без лишней скромности можем сказать, что мы инноваторы и идём впереди всей 
	кредитной системы! Благодаря инновационному подходу мы можем себе позволить 
	закрывать глаза на кредитную историю и не требовать с заёмщиков справки, 
	залог. 
В числе прочих способов повышения лояльности клиентов и уменьшения количества просроченных и безнадёжных кредитов нами используется метод "пряника" 
в отличии от методов используемых остальными кредитными организациями. </p>
		
<h2 style="margin:25px 0 0 0;">Низкий рейтинг чреват:</h2>

	<p style="text-indent:0">1) Понижением внимания к Вашей заявке и Вашим 
	пожеланиям;<br>2) Вы 
	получите наименее выгодные условиями кредитования по сравнению с другими 
	заёмщиками инвестора;<br>
	3) Дата выдачи кредита может быть назначена в некомфортное для Вас время;<br>
	4) Ожидание выдачи кредита может длиться дольше;<br>
	5) Единоразовая комиссия инвестора может быть больше чем у других 
заёмщиков инвестора;<br>6) Мы можем затребовать от Вас гарантии Вашей надёжности 
	и платежеспособности;<br>7) Ваша заявка на кредит, а также все обращения в службу поддержки 
	возможно будут 
обрабатываться с пониженным приоритетом, то есть скорость ответа на Ваши 
обращение может быть дольше регламентированного времени. 	</p>

<h2 style="margin:25px 0 0 0">Какие факторы влияют на рейтинг надёжности?</h2>
<h2 style="margin:15px 0 0 0">ОТРИЦАТЕЛЬНО:</h2>
	<p style="margin:5px 20px 0"><strong>ЗаЗадержка с выполнением инструкций и/или не выполнение их в озвученные нами сроки.</strong></p>
	<p style="margin:5px 20px 0">Вполне очевидно и мы везде неоднократно акцентируем внимание на том, что любая задержка с выполнением инструкций влечёт за собой 
	прямо пропорциональную задержку выдачи, тоесть кредит можно получить на сутки раньше, если на сутки раньше отправить 
	инвестору письмо и соответственно если на три дня 
	позже отправить письмо, то и дата выдачи сместится на три дня, однако помимо того, что подобная халатность естественным образом прямо пропорционально
	негативно влияет на сроки выдачи кредита, но также это чревато понижением рейтинга заёмщика, в следствии чего, мы со своей стороны будем считать честным, 
	допуская возможность игнорирования Ваших пожеланий по поводу сроков выдачи в 
	пользу пожеланий заёмщиков которые все инструкции выполняли в срок.
	<p style="margin:5px 20px 0"><strong>Молчаливый отказ от одобренного 
	кредита, особенно на завершающих стадиях (перед самой выдачей).</strong><p style="margin:5px 20px 0">
	Мы, увы, не экстрасенсы и мыслей НЕ читаем, а потому если у клиента 
	поменялись планы, возникли форсмажоры, командировки, болезни или в принципе 
	отпала потребность в кредите, нас нужно обязательно уведомлять об этом сразу 
	же. Сейчас все телефоны и даже некоторые микроволновки и утюги имеют 
	возможность выхода в интернет, а потому мы не понимаем когда нам говорят "у 
	меня не было возможности сообщить". Если Вам не оторвало руки - возможность 
	сообщить у Вас была и есть.<p style="margin:5px 20px 0"><strong>Хамство и неадекватное общение с нашей службой поддержки и/или инвестором.</strong></p>
<p style="margin:5px 20px 0">Мы не можем не отметить, что в данной сфере правило "<em>клиент всегда прав</em>" работает несколько иначе... 
Вернее оно вообще не работает. Мы как никто 
защищаем права заёмщиков которые получили у нас кредит. Мы постоянно проводим
<a href="../akciya.php">акции </a>(предоставляем <a href="../anticollektor.php">антиколлекторскую помощь</a>, 
