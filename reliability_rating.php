т.п.), НО если Вы ещё не получили кредит, то имейте ввиду - ВЫ ПРИШЛИ "ПРОСИТЬ", 
а мы в свою очередь предлагаем Вам условия, озвучиваем, сроки и регламент. 
Вам до получения кредита, предоставляется возможность лишь ЛИБО согласиться с ними 
и получить кредит ЛИБО отказаться от кредита, если Вас что-то не устраивает. Мы не торгуемся и не работаем на Ваших условиях и тем 
более мы не будем терпеть быдло. В сущности тут как и в любой сфере важно не 
путать наглость со своими правами и тогда мы сработаемся.</p>
<h2 style="margin:15px 0 0 0">ПОЛОЖИТЕЛЬНО:</h2>
	<p style="margin:15px 0 0 0">Положительно влияет на рейтинг всё прямо противоположное. 
	Выполняйте в срок инструкции, будьте всегда на связи, уведомляйте о любых 
	изменениях, будьте тактичными и вежливыми в общении и мы сделаем всё, для 
	того, чтобы Вы получили максимально выгодный кредит, на наилучших условиях и в 
	сжатые сроки!</p>

<h2 style="margin:25px 0 0 0">Немного подробностей о том, как мы пришли к созданию рейтинга заёмщиков</h2>
<p style="margin:15px 0 0 0">В 2013 году, то есть уже буквально через год после 
того как нами была создана программа кредитования через не классических 
инвесторов (кредитных доноров) мы начали понимать, что несмотря на то, что 
действительно предоставляем удивительную возможность получить кредит всем кому 
отказывают в прочих кредитных организациях, тем не менее огромное количество 
заёмщиков халатно относятся к предоставляемому нами шансу. При этом, как бы мы 
не старались, но угодить абсолютно всем заёмщикам мы не можем.<p style="margin:15px 0 0 0">Каждый день мы думаем над тем как сделать наши кредиты максимально выгодными для наших заёмщиков и делаем мы это 
	на самом деле в первую очередь просто потому, что чем дешевле займ, тем меньше с ним проблем, просрочек и невозвратов.
	В итоге мы пришли к тому, что заёмщиков нужно делить на категории и в 
	течении двух лет мы разрабатывали идеальную рейтинговую систему.</p>
	<p style="margin:15px 0 0 0">Теперь мы имеем возможность сделать кредитные 
	программы ещё более гибкими за счёт того, что можем себе позволить уделять 
	разным категориям заёмщиков разное количество внимания и предоставлять им 
