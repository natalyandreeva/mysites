<h2 style="display:block; margin:10px 20px">Предложение для посредников и брокеров</h2>
<p>На текущий момент у нас по сути нет недостатка в заёмщиках и мы надеемся, что 
объяснять "почему" - не нужно. В контексте этого сообщаем, что на сегодняшний день мы 
практически не заинтересованы в сотрудничестве с посредниками-брокерами которые имеют 
возможность привлекать только заёмщиков. Однако если вы представляете крупное 
брокерское объединение имеющее возможность снабжать нас большим количеством 
потенциальных заёмщиков (минимум 10 в день), присутствующее в нескольких городах страны, 
то Вы можете 
обратиться в <a href="../fb.php">службу поддержки</a> и озвучить о своём желании 
наладить сотрудничество.</p>
<p>Комиссия за привлечённого заёмщика которую мы выплачиваем равна ни много ни мало 
- 0,5% от суммы, которую получил заёмщик, по факту получения заёмщиком кредита. НО 
у нас есть условие - если Вы отпрвляете заёмщика к нам - Вы не должны взымать с 
него никаких комиссий! </p>
	<p>Как уже было отмечено выше, заёмщики нам на самом деле малоинтересны, ибо в них недостатка нет вообще и в принципе.
Нам гораздо интересней сотрудничество с посредниками которые могут привлекать 
	инвесторов. Выплата за привлечённого инвестора составляет 3-4% от дохода 
	инвестора.</p>
<h2 style="display:block; margin:10px 20px">Обращение к действующим инвесторам и 
кредитным донорам</h2>
<p>Инвесторов постоянно не хватает и несмотря на оптимизацию работы, и качественное распределением средств по системе, существует большая нехватка 
инвесторского ресурса. К сожалению, найти инвесторов  
"лобовой рекламой" как показала практика – не представляется возможным и эффективность любого типа рекламы очень низка. Наилучшим каналом привлечения 
инвесторов – является сарафанное радио или проще говоря «совет друга». 
</p>
<p>Мы очень надеемся, на то, что Вы (действующие 
инвесторы), как люди ищущие возможность заработать обратите внимание ещё и на схему сотрудничества с нами по привлечению новых 
инвесторов. Вероятно вокруг Вас каждый день 
крутятся ваши коллеги, которые вероятно тоже имеют средства для инвестирования. Предложите им заработать как Вы 
и вы получите 3-5% от дохода полученного вашим другом. Помимо этого вы вполне можете назначать ещё и свою комиссию будущему 
инвестору «за совет».</p>
<h2 style="display: block; margin:10px 20px">Предложение для наших 
заёмщиков (возможно даже на дому)</h2>
<p><strong>Обращаем Ваше внимание на то, что заёмщикам для заключения договора сотрудничества НЕ нужно подавать никаких заявок!</strong> Если Вы уже подали заявку на кредит, то этого достаточно! Не вводите нас в заблуждение и не подавайте заявки от имени брокеров или 
инвесторов!</p>
	<p>Несмотря на то, что <a href="sotr.php">тут</a> мы 
	пишем, что потребности в привлечении заёмщиков у нас нет, тем не 
	менее нашим заёмщикам мы предоставляем возможность работать с нами в этом 
	направлении. Вознаграждение как таковое мы не выплачиваем, однако за 
	качественные заявки мы списываем с Вас часть долга по кредиту перед нами. За 
	каждую проверенную и подтверждённую заявку на кредит мы списываем 0,5 евро, 
	а если заёмщик предложенный Вами получит у нас кредит, то мы списываем 50-100 
	евро в зависимости от суммы кредита взятого заёмщиком привлечённым Вами. У 
	нас уже есть несколько десятков заёмщиков которые получили 
	достаточно большие кредиты у нас и ни копейки по нему нам 
	не заплатили, полностью погасив кредит за счёт заёмщиков которых они для нас 
	привлекали.</p>
	<p>Учитывая тот факт, что положительные решения мы принимаем в 60% случаев и 
	в итоге примерно каждый третий заёмщик из подавших заявки получает у нас 
