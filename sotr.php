	перед нами за счёт дохода который Вы будете получать!</p>
	<p>Из обязательных условий для сотрудничества - Вы должны прилежно погашать 
	кредит перед нами. Только заёмщики, которые платят вовремя часть кредита которая 
	не была списана с помощью дохода от привлечения заёмщиков - могут воспользоваться данным предложением. Тем 
	самым мы стимулируем наших заёмщиков не допускать просрочек! Одна 
	единственная просрочка приведёт к отключение Вас от программы.</p>

